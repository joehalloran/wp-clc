                    <p class="lead">ReflectED is an approach to learning, developed by Rosendale Primary school, that teaches and develops children’s metacognition skills (learning how they learn). It supports and improves attainment, especially amongst disadvantaged pupils, and aims to help learners of all backgrounds develop the tools to make excellent progress in their learning and fulfil their potential.</p>
                    <p>ReflectED teaches children the skills of reflection and how to record their learning moments and strategies. Teachers can also look across these reflections to understand what pupils are enjoying or struggling with, and identify specific pupil needs.</p>
                    <p>Evidence suggests the metacognitive skills children develop through ReflectED will significantly enhance their learning.</p>
                    <h2>Our role</h2>
                    <p>London CLC is providing technical advice and support on the Reflected project, especially around the use of Cloud based systems to record pupil’s reflections. We have also been working directly with schools participating in the trial, providing advice and training on all aspects of the ReflectED programme.</p>
                    <h2>Research findings</h2>
                    <p>Research published in November 2016 found that ReflectED improved attainment in maths – but had negative impacts on reading.</p>
                    <p>The evaluation report, published earlier this month, found pupils made an average of four additional months progress in maths, but made two months less progress in reading.</p>
                    <p>The 2014-15 Education Endowment Foundation (EEF) trial found that year 5 pupils who took part in the ReflectED programme &#8220;developed  a more positive attitude to maths&#8221; and that teachers &#8220;praised the systematic approach ReflectED brought to their attempts to develop metacognition&#8221;.</p>
                    <p>In the year-long trial, year 5 pupils across 30 primary schools completed 28 ReflectED lessons. Pupils were also encouraged to use reflection techniques throughout the rest of the curriculum.</p>
                    <p>The EEF report  also suggested that expanding the trial may have improved results. Participating teachers said that &#8220;ReflectED would work best as a whole school programme&#8221;. Researchers suggested that &#8220;the programme might have a greater impact after the first year of delivery&#8221; as teachers became familiar with the ReflectED approach.</p>
                    <p>The finding in the trial have &#8220;moderate to high security&#8221;, scoring 4/5 on the EEF rating system. Researchers also found the programme to be extremely cost effective, at £18.72 per pupil, scoring 1/5 on the EEF cost rating system.</p>
